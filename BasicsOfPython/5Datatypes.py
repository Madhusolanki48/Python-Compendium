# PYTHON DATATYPES: Numeric , Sequence , Dictionary , Boolean , Set
# Numeric : Integer , Floats , Complex Numbers
# Sequence : Strings , Lists , Tuples

# Strings
name="Jim"
type(name)
# <class 'str'>

# List   --mutable in nature
example_list=[1,'hello',4.5,"A"]
type(example_list)
# <class 'list'>
print(example_list)

#Tuples  --immutable in nature
example_tuple=[1,'hello',4.5,"A"]
print(example_tuple[1])

# Dictionary  --key value pair
ed={'a':22,'b':44.4}
print(ed['a'])

# Boolean  --to check whether the value is true or false
# type(True)
# <class 'bool'>
# type(False)
# <class 'bool'>

# Set
example_set={1,'hello',4.5,"A"}
type(example_set)

a=10.0
print(type(a))

b='abhdghdjkd'
print(type(b))

